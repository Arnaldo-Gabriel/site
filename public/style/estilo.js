
/*funcion para definir cor do botão principal*/
function move(){
    const colorPassMouse = window.document.getElementById('link');
    colorPassMouse.style.background = '#c9d6df';
}
function retire(){
    const colorExitMouse = window.document.getElementById('link');
    colorExitMouse.style.background = 'white';
}


